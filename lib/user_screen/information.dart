class PersonalInfo {
  final String phone;
  final String firstName;
  final String lastName;
  final String email;
  final DateTime dob;
  final String address;
  final int height;
  final int weight;
  final int muscleRatio;
  final int fatRatio;
  final int visceralFatLevels;

  PersonalInfo({
    required this.phone,
    required this.firstName,
    required this.lastName,
    required this.email,
    required this.dob,
    required this.address,
    required this.height,
    required this.weight,
    required this.muscleRatio,
    required this.fatRatio,
    required this.visceralFatLevels,
  });
}
